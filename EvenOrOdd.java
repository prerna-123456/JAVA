package JAVA;

public class EvenOrOdd {
    public static void main(String[] args) {
        int n = 15;

        if (n % 2 == 0) {
            System.out.println("Checking if the number is even or odd...");
            System.out.println(n + " is even.");
        } else {
            System.out.println(n + " is odd.");
        }
    }
}
