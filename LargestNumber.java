package JAVA;

public class LargestNumber {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        if (a > b) {
            System.out.println(a + " is the largest.");
        } else if (a < b) {
            System.out.println(b + " is the largest.");
        } else {
            System.out.println("Both numbers are equal.");
        }
    }
}

